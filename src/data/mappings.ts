export function getNationalityFlagIso(id: number): string | null {
  if (!Object.prototype.hasOwnProperty.call(nationalityFlagIso, id)) return null
  return nationalityFlagIso[id] ?? null
}

export interface NationalitySelectOption {
  value: number
  name: string
  flagIso: string | null
}

/** 下拉用：来自 nationalities，仅国家名 + 可选国旗 ISO 后缀 */
export function getNationalitySelectOptions(): NationalitySelectOption[] {
  return Object.entries(nationalities)
    .map(([value, name]) => {
      const v = Number(value)
      return {
        value: v,
        name,
        flagIso: getNationalityFlagIso(v),
      }
    })
    .sort((a, b) => a.value - b.value)
}

// 获取国籍名称
export function getNationalityName(value: number): string {
  return nationalities[value] || `国家${value}`
}
